print('Operações Aritméticas no Python')

n1 = 5
n2 = 2

print(f'{n1} + {n2} == {n1 + n2}')
print(f'{n1} - {n2} == {n1 - n2}')
print(f'{n1} * {n2} == {n1 * n2}')
print(f'{n1} / {n2} == {n1 / n2}')
print(f'{n1} ** {n2} == {n1 ** n2}')
print(f'{n1} // {n2} == {n1 // n2}')
print(f'{n1} % {n2} == {n1 % n2}')

print('Outras Demonstrações:')

print(f'5 + 3 * 2 == {5 + 3 * 2}')
print(f'3 * 5 + 4 ** 2 == {3 * 5 + 4 ** 2}')
print(f'3 * (5 + 4) ** 2 == {3 * (5 + 4) ** 2}')
