print('Operações Aritméticas')

n1 = 5
n2 = 2

print(f'{n1} + {n2} == {n1 + n2}')
print(f'{n1} - {n2} == {n1 - n2}')
print(f'{n1} * {n2} == {n1 * n2}')
print(f'{n1} / {n2} == {n1 / n2}')
print(f'{n1} ** {n2} == {n1 ** n2}')
print(f'{n1} // {n2} == {n1 // n2}')
print(f'{n1} % {n2} == {n1 % n2}')
